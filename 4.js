console.log("Contagem com while:");
while (contador <= 5) { // Enquanto contador for menor ou igual a 5. Aqui é até onde a contagem vai, nesse caso até 5
 console.log(contador); // Mostra o valor
 contador++; // Incrementa +1 no contador a cada repetição
}