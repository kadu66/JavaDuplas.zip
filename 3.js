// 3) FOR

// O for é usado quando sabemos quantas vezes queremos repetir
// Aqui vamos contar de 1 até 5
console.log("Contagem com for:");
for (let i = 1; i <= 5; i++) { //variavel de nome "i" que representa o número que vai começar a contagem 
 // 'i' começa em 1, e enquanto for menor ou igual a 5, repete
 console.log(i); // Mostra o valor de 'i' a cada repetição
}